/*
    Escreeva uma função que receba um número negatico e retorne um número positivo;
    Dica: Utilize a função Math.abs
*/

function retornaPositivo(num) {
    return Math.abs(num);
}

console.log(retornaPositivo(-20));
console.log(retornaPositivo(-18));
console.log(retornaPositivo(0));
console.log(retornaPositivo(-180));
console.log(retornaPositivo(20));
